		local character = player.Character
		if character then
			local humanoid = character:FindFirstChildOfClass("Humanoid")
			if humanoid then
				humanoid.WalkSpeed = speed
				if currentSpeedChange then
					currentSpeedChange:Disconnect()
				end
				currentSpeedChange = game:GetService("RunService").Stepped:Connect(function()
					humanoid.WalkSpeed = speed
				end)
			end
		end
	end
	
	-- Проверка, активно ли текстовое поле
	local function isTextBoxFocused()
		return UserInputService:GetFocusedTextBox() == textbox
	end
	
	-- Обработчик события FocusLost
	textbox.FocusLost:Connect(function(enterPressed)
		if enterPressed then
			local player = game.Players.LocalPlayer
			local input = textbox.Text
			local speed = tonumber(input)
			
			if speed then
				changeSpeed(player, speed)
			else
				warn("Please enter a valid number!")
			end
		end
	end)
	
