		playerFrame.OriginalName.Text = "@"..player.Name
		playerFrame.PlayerAvatar.Image = Players:GetUserThumbnailAsync(player.UserId, Enum.ThumbnailType.HeadShot, Enum.ThumbnailSize.Size100x100)
		
